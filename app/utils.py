def get_setor_por_identificador(identificador):
    """
    Retorna o nome do setor solicitante com base no seu número identificador.
    O identificador é convertido para string para garantir a correspondência
    com as chaves do dicionário.
    Retorna None se o identificador não for encontrado ou for None.
    """
    if identificador is None:
        return None
    # .strip() para remover possíveis espaços extras do input do usuário ou dos dados
    return UNIDADES_POR_IDENTIFICADOR.get(str(identificador).strip())

def get_todos_identificadores():
    """
    Retorna uma lista de todos os números identificadores de unidade disponíveis.
    Útil para validações ou popular selects, se necessário.
    """
    return list(UNIDADES_POR_IDENTIFICADOR.keys())

def get_todos_setores():
    """
    Retorna uma lista de todos os nomes de setores solicitantes disponíveis.
    """
    return list(UNIDADES_POR_IDENTIFICADOR.values())

